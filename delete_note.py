"""
На этом этапе мы напишем программу, которая позволяет пользователю возможность:
1. Удалять заметки из списка.
2. Хранить заметки в виде списка словарей.
3. Реализовать функционал:
  - Удаление заметки по имени пользователя.
  - Удаление заметки по заголовку.
4. Запрашивать критерий для удаления (например, имя пользователя или заголовок).
5. Удалять все заметки, соответствующие введённому критерию.
6. Если заметок для удаления не найдено, выводить соответствующее сообщение.
7. После удаления оставлять остальные заметки в списке.
"""
# Список заметок, где каждая заметка представлена словарем
notes = [
    {
        'user': 'Алексей',
        'title': 'Список покупок',
        'description': 'Купить продукты на неделю'
    },
    {
        'user': 'Мария',
        'title': 'Учеба',
        'description': 'Подготовиться к экзамену'
    }
]

def display_notes():
    """Отображает текущие заметки."""
    print("Текущие заметки:")
    for index, note in enumerate(notes, start=1):
        print(f"{index}. Имя: {note['user']}")
        print(f"   Заголовок: {note['title']}")
        print(f"   Описание: {note['description']}\n")

def delete_notes(criteria):
    """Удаляет заметки по критериям (имя пользователя или заголовок)."""
    global notes
    original_count = len(notes)
    notes = [note for note in notes if note['user'] != criteria and note['title'] != criteria]
    if len(notes) < original_count:
        print("Успешно удалено. Остались следующие заметки:")
        display_notes()
    else:
        print("Заметок с таким именем пользователя или заголовком не найдено.")

def main():
    """Основная функция программы."""
    while True:
        display_notes()
        criteria = input("Введите имя пользователя или заголовок для удаления заметки: ")

        if criteria.strip().lower() == 'exit':
            print("Выход из программы.")
            break

        delete_notes(criteria)
if __name__ == '__main__':
    main()