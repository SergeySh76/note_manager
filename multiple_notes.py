def main():
    print("Добро пожаловать в 'Менеджер заметок'! Вы можете добавить новую заметку.")

    notes = []  # Список для хранения заметок

    while True:
        note = {}  # Создаем словарь для одной заметки

        note['Имя пользователя'] = input("Введите имя пользователя: ")
        note['Заголовок заметки'] = input("Введите заголовок заметки: ")
        note['Описание заметки'] = input("Введите описание заметки: ")
        note['Статус заметки'] = input("Введите статус заметки (новая, в процессе, выполнено): ")
        note['Дата создания'] = input("Введите дату создания (день-месяц-год): ")
        note['Дедлайн'] = input("Введите дедлайн (день-месяц-год): ")

        notes.append(note)  # Сохраняем заметку в списке

        another = input("Хотите добавить ещё одну заметку? (да/нет): ").strip().lower()
        if another != 'да':
            break  # Прерываем цикл, если пользователь не хочет добавлять новую заметку

    # Выводим все заметки
    print("\nСписок заметок:")
    for i, note in enumerate(notes, start=1):
        print(f"{i}. Имя: {note['Имя пользователя']}")
        print(f"   Заголовок: {note['Заголовок заметки']}")
        print(f"   Описание: {note['Описание заметки']}")
        print(f"   Статус: {note['Статус заметки']}")
        print(f"   Дата создания: {note['Дата создания']}")
        print(f"   Дедлайн: {note['Дедлайн']}\n")

if __name__ == "__main__":
    main()
