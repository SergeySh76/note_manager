"""
На этом этапе мы начинаем добавлять динамическое
взаимодействие и возможность обработки нескольких заметок.
Порядок выполнеия задания:
1. Создаем программу, которая будет запрашивать у пользователя
заголовки заметки.
2. Используем цикл, чтобы позволить пользователю добавлять столько
заголовков, сколько потребуется. Пользователь должен иметь возможность
завершить ввод, указав специальную команду (например, "стоп" или пустой ввод).
3. После завершения ввода программа должна вывести список всех добавленных
заголовков."""

# Инициализация списка для хранения заголовков
titles = []

# Цикл для добавления заголовков
while True:
    title = input("Введите заголовок (или оставьте пустым для завершения):")

    # Проверка на завершение ввода
    if not title:
        break

    # Добавление заголовка в список
    titles.append(title)

# Вывод списка заголовков
print("Заголовки:")
for title in titles:
    print(title)
