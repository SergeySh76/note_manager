 # Инициализация списка для хранения заголовков
titles = []

 # Цикл для добавления заголовков
while True:
    title = input("Введите заголовок (или оставьте пустым для завершения): ")

 # Проверка на завершение ввода
    if not title:
       break

 # Добавление заголовка в список
    titles.append(title)

# Вывод списка заголовков
print("Заголовки:")
for title in titles:
    print(f"- {title}")