"""
На этом этапе мы будем проверять истечение срока выполнения задачи.
В ходе выполнения задания нам необходимо:
1. Получить текущую дату с помощью встроенной библиотеки Python (datetime).
2. Запросить у пользователя дату дедлайна (в формате день-месяц-год).
3. Сравнить дату дедлайна заметки с текущей датой:
   - Проверить, находится ли дедлайн в прошлом, настоящем или будущем;
   - Реализовать разницу в днях между текущей датой и дедлайном.
4. Вывести соответствующее сообщение:
   - Если дедлайн истёк, предупредить об этом пользователя;
   - Если дедлайн не истёк, сообщает, сколько времени осталось до истечения срока.
"""
#Получаем текущую дату
import datetime
def check_deadline(issue_date_str):

    # Определяем формат даты
    date_format = "%d-%m-%Y"
    try:
        # Преобразуем строку даты в объект datetime
        issue_date = datetime.datetime.strptime(issue_date_str, date_format)
    except ValueError:
        print("Неверный формат даты! Пожалуйста, используйте формат: день-месяц-год (DD-MM-YYYY).")
        return

    # Получаем текущую дату
    current_date = datetime.datetime.now()

    # Сравниваем даты
    if issue_date < current_date:
        print("Предупреждение: дедлайн истек!")
    elif issue_date == current_date:
        print("Сегодня последний день выполнения задачи!")
    else:
        days_remaining = (issue_date - current_date).days
        print(f"У вас еще {days_remaining} {'день' if days_remaining == 1 else 'дня'} до истечения дедлайна.")


if __name__ == "__main__":
    deadline_input = input("Введите дату дедлайна (день-месяц-год): ")
    check_deadline(deadline_input)